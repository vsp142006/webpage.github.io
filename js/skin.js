function productcall(data){
    document.querySelector("#product_section").innerHTML=""
    data.map(function(ele,i){
        div=document.createElement("div")

        
        p_tag=document.createElement("p")
        p_tag.innerText="BESTSELLER"
        
        
        p_image=document.createElement("img")
        p_image.setAttribute("src",ele.image)

        p_title=document.createElement("h4")
        p_title.innerText=ele.name

        p_price=document.createElement("p")
        p_price.innerHTML="MRP: "+'<i class="fas fa-indian-rupee-sign"> </i>'+" "+ele.mrp


        p_star=document.createElement("p")
        let bag=""
        for(let i=0; i<ele.star;i++){
            bag+='<i class="fas fa-star"></i>'
            // p_star.append(x)
        }
        p_star.innerHTML=bag


        P_rating=document.createElement("p")
        P_rating.innerText="("+ele.rating+")"

        p_shade=document.createElement("p")
        p_shade.innerText=ele.shade+" Shade"

        p_button=document.createElement("button")
        p_button.innerText="Add To Cart"
        p_button.addEventListener("click",function(event){
          if(account!=null && Number(cart_i.length)<7){
            cart_i.push(ele)
            localStorage.setItem("cart_data",JSON.stringify(cart_i))
            window.location.reload()
          }else if(account==null){
            location.href="register.html"
          }
          else{
            alert("Cart Length should be less than 7")
          }
          
        })

        div.append(p_tag,p_image,p_title,p_price,p_star,P_rating,p_shade,p_button);
        document.querySelector("#product_section").append(div)
    })
}


