// <-------it's a section for name wise filter---->
cart_i=JSON.parse(localStorage.getItem("cart_data"))|| []
account=localStorage.getItem("email")

document.querySelector("#name").addEventListener("change",function(){
    selected=document.querySelector("#name").value
    if(selected==""){
        productcall(skin_item)
    }
    else if(selected=="Ascending"){
        skin_item.sort(function(a,b){
            if(a.name<b.name){return -1}
            if(a.name>b.name){return 1}
        })
        
    }
    else{
        skin_item.sort(function(a,b){
            if(a.name<b.name){return 1}
            if(a.name>b.name){return -1}
        })
        
    }  
    productcall(skin_item)
})

// it;s is the section for price wise filter------------->

document.querySelector("#price").addEventListener("change",function(){
    selected=document.querySelector("#price").value

    if(selected==""){
        productcall(skin_item)
    }
    else if(selected=="low To High"){
        skin_item.sort(function(a,b){return a.mrp-b.mrp})
    }
    else if(selected=="high to low"){
        skin_item.sort(function(a,b){return b.mrp-a.mrp})
        
    }
    productcall(skin_item)
})


// This section for filter via rating wise-------->

document.querySelector("#filter_section>#rating").addEventListener("change", function(){
    selected=document.querySelector("#rating").value
    filtered=skin_item.filter(function(ele){
        
        if(selected==""){
           return skin_item
        }
        else if(Number(selected)==Number(ele.star)){
            return ele
        }
        
    })
   productcall(filtered)
})




